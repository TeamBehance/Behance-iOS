import UIKit

extension UIColor {
    
    @nonobjc class var behanceBlack: UIColor {
        return UIColor(red: 0.0, green: 0.0, blue: 0.0, alpha: 1.0)
    }
    
    @nonobjc class var behanceDarkgray: UIColor {
        return UIColor(red: 0.26, green: 0.26, blue: 0.29, alpha: 1.0)
    }
    
    @nonobjc class var behanceGray: UIColor {
        return UIColor(red: 0.63, green: 0.63, blue: 0.63, alpha: 1.0)
    }
    
    @nonobjc class var behanceLightgray1: UIColor {
        return UIColor(red: 0.7, green: 0.7, blue: 0.7, alpha: 1.0)
    }
    
    @nonobjc class var behanceLightgray2: UIColor {
        return UIColor(red: 0.94, green: 0.94, blue: 0.94, alpha: 1.0)
    }
    
    @nonobjc class var behanceBlue: UIColor {
        return UIColor(red: 0.0, green: 0.34, blue: 0.99, alpha: 1.0)
    }
    
    @nonobjc class var behanceRed: UIColor {
        return UIColor(red: 0.77, green: 0.27, blue: 0.26, alpha: 1.0)
    }
}
